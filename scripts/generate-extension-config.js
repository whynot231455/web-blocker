const fs = require('fs');
const path = require('path');

// Paths
const envPath = path.join(__dirname, '../website/.env.local');
const configJsPath = path.join(__dirname, '../extension/lib/config.js');

function generateConfig() {
  console.log('Generating extension config from website/.env.local...');

  if (!fs.existsSync(envPath)) {
    console.error('Error: website/.env.local not found.');
    process.exit(1);
  }

  const envContent = fs.readFileSync(envPath, 'utf8');
  const lines = envContent.split('\n');
  
  const envVars = {};
  for (const line of lines) {
    const [key, ...valueParts] = line.split('=');
    if (key && valueParts.length > 0) {
      envVars[key.trim()] = valueParts.join('=').trim().replace(/['"]/g, '');
    }
  }

  const supabaseKey = envVars['NEXT_PUBLIC_SUPABASE_ANON_KEY'];
  const supabaseUrl = envVars['NEXT_PUBLIC_SUPABASE_URL'];

  if (!supabaseKey || !supabaseUrl) {
    console.error('Error: Required environment variables (KEY or URL) not found in .env.local');
    process.exit(1);
  }

  const configContent = `/**
 * AUTO-GENERATED FILE - DO NOT EDIT MANUALLY
 * Generated by scripts/generate-extension-config.js
 * This file is gitignored to prevent leaking secrets.
 */
globalThis.CTRL_BLCK_CONFIG = {
    supabaseUrl: '${supabaseUrl}',
    supabaseKey: '${supabaseKey}'
};
`;

  fs.writeFileSync(configJsPath, configContent);
  console.log(`Successfully generated ${configJsPath}`);
}

generateConfig();
